<h3>Assigning the blog page</h3>

<p>
    The first blog method we will cover is the standard one , setting up an assigned blog or “post” page.
    It’s as simple as making a new page, and then adjusting WordPress to use that page as the post page.
</p>

<p>
    Follow these Steps to assign a Posts Page
</p>

<ol class="index-list">
    <li>Navigate to Pages and click Add New.</li>
    <li>Input a new name for your page, then find the Page Attributes box on the right side.
    </li>
    <li>Select the Default page template from the dropdown list.</li>
    <li>Select any number of page options in the "Page Options Box".</li>
    <li>Click "Publish" to save the page.</li>
    <li>To set the posts page, navigate to Settings > Reading and select your
        blog page name for the Posts Page. After that your blog posts will be pulled 
        and placed on the page automatically.
    </li>
</ol>
