<h3>Общие</h3>

<p>Секция General содержит глобальные настройки Cherry Framework</p>

<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Favicon image.</dt>
            <dd>Icon image that is displayed in the browser address bar and browser tab heading. Max icon (.ico) size is
                32x32 px. You can also upload favicon for retina displays. Max retina icon size: 152x152 px.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Режим обслуживания</dt>
            <dd>
                Enable/disable maintenance mode. Logged in administrator gets full access to the site while regular
                visitors
                will be redirected to the page chosen below. You can also preview the maintenance page without enabling
                maintenance mode.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Страница режима обслуживания</dt>
            <dd>
                Select a page that regular visitors will see if maintenance mode is enabled. You can select any
                WordPress
                page here including your custom ones.
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Smooth scroll на странице</dt>
            <dd>
                Enable/disable smooth vertical mousewheel scrolling (Chrome browser only).
            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Пользовательский CSS</dt>
            <dd>
                Текстовый редактор для пользовательского CSS.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Landing page</dt>
            <dd>
                Определяет страницу на которой будет использована “якорная” навигация.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>To Top</dt>
            <dd>
                Определяет вывести/скрыть кнопку “to top”.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>404 content</dt>
            <dd>
                Визуальный редактор для создания содержимого 404 страницы.
            </dd>
        </dl>
    </li>
    <li>
        <dl class="inline-term">
            <dt>Google Analytics Code</dt>
            <dd>
                Текстовый поле для вставки кода гугл аналитики.
            </dd>
        </dl>
    </li>
</ul>

