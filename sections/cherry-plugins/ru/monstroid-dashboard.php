	<p>
		Кроме того, у Вас есть возможность скачать архивы с сервера из папки <strong>/wp-content/uploads/update-backups/</strong>. Актуальную версию темы можно скачать, нажав на кнопку <strong>“Download latest Monstroid version”</strong>
	</p>

