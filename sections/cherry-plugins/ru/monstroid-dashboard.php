	</p>

	<p>
		Предварительно можно проверить актуальные обновление, необходимо нажать на кнопку “Check Update”.</ br> Нажмите кнопку “Update”, после этого, плагин сделает резервную копию вашей текущей темы и обновит устновленную.
	</p>

