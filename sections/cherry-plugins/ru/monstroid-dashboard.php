	<p>
		После завершения обновления, вам будет предложено сохранить резервную версию темы на ваш компьютер.
	</p>

	<figure class="img-polaroid">
		<img src="img/monstroid/plugins/dashboard_save_archive.png" alt="" style="opacity: 1;"><i class="icon-search"></i><span></span>
