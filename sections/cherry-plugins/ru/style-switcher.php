	<h3>Cherry Style Switcher</h3>

	<p><a href="https://github.com/CherryFramework/cherry-style-switcher">Cherry Style Switcher</a> - плагин предназначенный для переключения скинов, задания лейаутов, размешения информационных блоков в хедере и футере.</p>

	<p>
		На главной странице “Cherry => Option => Style Switcher”
	</p>

	<p>
		Более подробно о настройках в опциях описано здесь - <a href="">Style Switcher options</a>
	</p>

