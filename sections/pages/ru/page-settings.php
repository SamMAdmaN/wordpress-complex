	<h3>Настройки страницы</h3>

	<p>
	    Кроме глобальных настроек которые размещены в опциях, у Вас есть возможность настроить параметры для конкретной страницы или поста.
	</p>

	<h4>
		Дополнительные опции для страницы
	</h4>

	<figure class="img-polaroid"><img src="img/cherryframework/page_settings_breadcrums.png" alt=""></figure>

	<p>
	    Настраиваем вывод цепочки постраничной навигации и заголовка страницы, доступные параметры:
	</p>

	<ul class="marked-list">
		<li>
			Don’t display (Не показывать)
		</li>
		<li>
			Show both - breadcrumbs and title (Показывать навигацию и заголовок)
		</li>
		<li>
			Show only page title (Показывать только заголовок)
		</li>
		<li>
			Show only breadcrumbs (Показывать только навигацию)
		</li>
	</ul>

