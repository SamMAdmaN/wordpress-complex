<h3>Booster for WooCommerce</h3>

<p>The <a href="https://wordpress.org/plugins/woocommerce-jetpack/"> Booster for WooCommerce plugin</a> provides you with an additional set of tools and features that ensure enhanced functionality of the site and allow visitors to shop and navigate much easily.</p>


<figure class="img-polaroid">
        <img src="projects/<?php echo $project;?>/img/third-party-plugins/booster.png">
</figure>

<h4>Installation</h4>
<ul class="index-list">
	<li>
	Upload the entire <strong>woocommerce-jetpack</strong> folder to the <strong>/wp-content/plugins/</strong> directory
	</li>
	<li>Activate the plugin through the 'Plugins' menu in WordPress</li>
	<li>Go to WooCommerce > Settings > Booster</li>
</ul>

