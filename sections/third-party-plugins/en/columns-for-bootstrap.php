<h4>Installing Colums for Bootstrap</h4>

<p>So, let's take a closer look at how to install this plugin</p>
<ol class="index-list">
	<li><p> Make sure that the current WordPress theme is built with Bootstrap</p></li>
	<li><p> Download archive and unzip in wp-content/plugins or install via Plugins - Add New</p></li>
	<li><p> Activate the Columns for Bootstrap plugi.</p></li>
	<li><p> Check the top of the Plugins page for any warnings</p></li>
	<li><p> Begin using the [column-group] and [column] shortcodes</p></li>
</ol>

