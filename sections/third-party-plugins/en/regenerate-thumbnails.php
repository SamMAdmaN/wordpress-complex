<h4>Installing Facebook Widget plugin</h4>

<ol class="index-list">
	<li><p> Go to your admin area and select Plugins -> Add new from the menu.</p></li>
	<li><p> Search for "Regenerate Thumbnails".</p></li>
	<li><p> Click install.</p></li>
	<li><p> Click activate.</p></li>
</ol>	

