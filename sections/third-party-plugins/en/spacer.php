<h3>Spacer</h3>

<p>This <a href="https://wordpress.org/plugins/spacer/screenshots/" target="_blank">WordPress plugin</a> adds a  a spacer button to the WYSIWYG visual editor which allows you to add precise custom spacing between lines in your posts and pages. 
