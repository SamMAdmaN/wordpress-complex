<h4>Installing Spacer</h4>

<p>So, let's take a closer look at how to install this plugin</p>
<ol class="index-list">
	<li><p> From WP admin > Plugins > Add New</p></li>
	<li><p> Search "Spacer" under search and hit Enter</p></li>
	<li><p> Click "Install Now"</p></li>
	<li><p> Click the Activate Plugin link</p></li>
</ol>

