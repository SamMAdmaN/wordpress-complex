<h4>Features</h4>
<p>After the plugin is installed and activated, you will see an additional menu tab in the left sidebar. Let's take a look how to create a new timeline. For that we need to click the "Add new button" and specify some information.</p>


<ul class="marked-list">
    <li>
        <dl class="inline-term">
            <dt>Enter title here</dt>
	            <dd>
	                Type in the title of the new property
	            </dd>
        </dl>
    </li>

    <li>
        <dl class="inline-term">
            <dt>Type in the description of the property in the content field </dt>
        </dl>
    </li>

</ul>    
