<h4>Installing Tiny Grid plugin</h4>

<p>So, let's take a closer look at how to install this plugin</p>
<ol class="index-list">
	<li><p> Upload the folder tiny-grid to the /wp-content/plugins/ directory</p></li>
	<li><p> Activate the plugin through the 'Plugins' menu in WordPress</p></li>
	<li><p> Use the grid buttons at the tinymce</p></li>
</ol>

<h4>Adding the grid</h4>
Let's create a new post and add new grid.

<ol class="index-list">
	<li><p> Click  the "Grid" icon at the top navigation panel</p></li>
	<li><p> Choose the size and  number of columns in a popup window</p></li>
</ol>

