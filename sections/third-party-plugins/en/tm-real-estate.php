<h4>Installing the TM Real Estate plugin</h4>

<ol class="index-list">
	<li><p> Upload the tm-real-estate-master folder to the /wp-content/plugins/ directory</p></li>
	<li><p> Activate the plugin through the 'Plugins' menu in WordPress</p></li>
	<li><p> That's it!</p></li>
</ol>	
<p>
	
