<h3>Trending Posts</h3>

<p>Trending Posts is another custom widget from TemplateMonster team. It is designed to add some cutting edge functionality to the site and allows users to rate and see the views count for each post. The plugin also allows you to filter the posts by several criteria:</p>
<ol class="index-list">
	<li><p> Number of comments</p></li>
	<li><p> Number of views</p></li>
	<li><p> By rate</p></li>
</ol>
	
