<h4>Installing Trending Posts plugin</h4>

<p>So, let's take a closer look at how to install this plugin</p>
<ol class="index-list">
	<li><p> Upload "TM Trending Posts" folder to the "/wp-content/plugins/" directory</p></li>
	<li><p> Activate the plugin through the "Plugins" menu in WordPress</p></li>
	<li><p> Navigate to the "Widgets" section to start customizing</p></li>
</ol>


