<h4>Installing WooCommerce Compare List</h4>

<ol class="index-list">
	<li><p> Upload plugin to the /wp-content/plugins/ directory</p></li>
	<li><p> Activate WooCommerce Compare List plugin through the 'Plugins' menu in WordPress</p></li>
	<li><p> Go to WooCommerce settings page and open Compare List tab </p></li>
	<li><p> Select compare page, which will display comparison table</p></li>
	<li><p> Go to WordPress permalink settings and resave settings to flush rewrite rules </p></li>	
</ol>

