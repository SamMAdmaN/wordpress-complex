<h4>Installing Woocommerce Social Media Share Buttons</h4>

<ol class="index-list">
	<li><p> Download the Woocommerce Social Media Share Buttons plugin.(woocommerce-social-media-share-buttons.zip file)</p></li>
	<li><p> Extract the zip file to the '/wp-content/plugins/' directory</p></li>
	<li><p> Activate the plugin through the 'Plugins' menu in the WordPress admin area</p></li>
	<li><p> Configur your share buttons via the 'Share Buttons' menu option in the left admin area menu</p></li>
</ol>

