<h3>WordPress Countdown</h3>

<p><a href="https://wordpress.org/plugins/widget-countdown/" target="_blank">WordPress Countdown</a> plugin is an nice tool to create and insert countdown timers into your posts/pages and widgets. </p>
	
<h4>Installing the WordPress Countdown plugin</h4>

<ol class="index-list">
	<li><p>Log in to your administration panel. </p></li>
	<li><p>Go to Plugins page, click on Add New, then click "Upload Plugin".</p></li>
	<li><p>Then click "Choose file" then select the WordPress Countdown plugin zip file.</p></li>
	<li><p>Install and activate our WordPress Countdown plugin.</p></li>
	<li><p>You can add Countdown timer from your pages/posts and widgets.</p></li>
</ol>
