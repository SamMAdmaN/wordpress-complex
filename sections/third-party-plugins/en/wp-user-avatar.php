<h4>Installing WP User Avatar</h4>

<ol class="index-list">
	<li><p> Download, install, and activate the WP User Avatar plugin.</p></li>
	<li><p> On your profile edit page, click "Edit Image". </p></li>
	<li><p> Choose an image, then click "Select Image". </p></li>
	<li><p> Click "Update Profile".</p></li>
	<li><p> Upload your own Default Avatar in your WP User Avatar settings (optional). You can also allow Contributors & Subscribers to upload avatars and disable Gravatar. </p></li>	
	<li><p> Choose a theme that has avatar support. In your theme, manually replace get_avatar with get_wp_user_avatar or leave get_avatar as-is. </p></li>
	<li><p> You can also use the [avatar_upload] and [avatar] shortcodes in your posts. These shortcodes will work with any theme, whether it has avatar support or not. </p></li>
</ol>

<p>Right after the installation, you will see a new tab on the left sidebar. Let's navigate to the "Settings" section.</p>
<figure class="img-polaroid">
	<img src="projects/<?php echo $project;?>/img/third-party-plugins/user_avatar_2.png">
</figure>

<p>Here you can see the list of settings that allow you to adjust the avatars in accordance with your preferences. Once the customization is complete, press the <strong>"Save Changes" </strong>button. </p>

