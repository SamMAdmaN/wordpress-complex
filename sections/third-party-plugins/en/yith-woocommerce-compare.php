<h3> YITH WooCommerce Compare</h3>

<p>
	<a href="https://wordpress.org/plugins/yith-woocommerce-compare/" target="_blank">YITH WooCommerce Compare plugin</a> is an extension of WooCommerce plugin that allow your users to compare some products of your shop. All products are saved in one complete table where the user can see the difference between the products. Each product features can be set with the woocommerce attributes in the product configuration. You can also add a simple widget with the list of products the users have added, where you can manage them. Also you can simply customize the compare table with your style, by editing the specific template.
</p>

