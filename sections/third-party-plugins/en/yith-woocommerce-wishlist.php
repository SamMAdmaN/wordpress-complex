<h3>Yith WooCommerce wishlist</h3>

<p>
	What can really make the difference in conversions and amount of sales? Without a doubt it is a freedom to share your own wishlist, even on social networks, increasing indirect sales: can you imagine the sales volume you can generate during holidays or birthdays, when relatives and friends will be looking for the wishlist of your clients to buy a gift?
</p>
<p>
	Offer your visitors a chance to add the products of your WooCommerce store to a wishlist page. With <a href="https://wordpress.org/plugins/yith-woocommerce-wishlist/">YITH WooCommerce Wishlist</a> you can add a link in each product detail page, in order to add the products to the wishlist page. The plugin will create the specific page and the products will be added to this page, and afterwards, you can add them to the cart or completely remove.
</p>

