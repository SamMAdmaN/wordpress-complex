<h3>Accordion</h3>
<p>This module allows you to add an accordion to the website. It will be a perfect solution when you need to display multiple items within a vertical list and save space on a single page. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title </dt>
                <dd>
    				The toggle title will appear above the content and when the toggle is closed.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Content </dt>
                <dd>
    				Here you can define the content that will be placed within the current tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on </dt>
                <dd>
    				This will disable the module on selected devices.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        