<h3>Audio</h3>
<p>Add audio files and audio player to the pages. It can be used for music sites when it is needed  to showcase the latest songs or any other audio files.  
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Audio </dt>
                <dd>
    				Define the audio file for use in the module. To remove an audio file from the module, simply delete the URL from the settings field.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Title </dt>
                <dd>
    				Define a title.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Artist Name </dt>
                <dd>
    				Define an artist name.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Album name</dt>
                <dd>
                    Define an album name.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Cover Art Image URL</dt>
                <dd>
                    Upload your desired image, or type in the URL to the image you would like to display.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will disable the module on selected devices.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        