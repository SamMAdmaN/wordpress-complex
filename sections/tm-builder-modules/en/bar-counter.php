<h3>Bar Counters</h3>
<p>Display stats on your website in an attractive way. It can be used in online CVs and resumes for indicating your skills and experience. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title </dt>
                <dd>
    				Input a title for your bar.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Percent </dt>
                <dd>
    				Define a percentage for this bar.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Background Color </dt>
                <dd>
    				This will adjust the color of the empty space in the bar (currently gray).
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Bar Background Color </dt>
                <dd>
                    This will change the fill color for the bar.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Use Percentages</dt>
                <dd>
                    Enable/disable percentages.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will disable the module on selected devices.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        