<h3>Blurb</h3>
<p>If you need to combine short text with images, blurbs will be a perfect solution for that. They can be inserted into any column created on the website. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title</dt>
                <dd>
    				The title of your blurb will appear in bold below your blurb image.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Url </dt>
                <dd>
    				If you would like to make your blurb a link, input your destination URL here.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Url Opens </dt>
                <dd>
    				Here you can choose whether or not your link opens in a new window.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Use Icon </dt>
                <dd>
                    Here you can choose whether icon set below should be used.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image</dt>
                <dd>
                    Upload an image to display at the top of your blurb.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image Alt Text</dt>
                <dd>
                    Define the HTML ALT text for your image here.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image/Icon Placement</dt>
                <dd>
                    Here you can choose where to place the icon.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image/Icon Animation</dt>
                <dd>
                   This controls the direction of the lazy-loading animation.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Text Orientation</dt>
                <dd>
                    This will control how your blurb text is aligned.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Content</dt>
                <dd>
                    Input the main text content for your module here.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                   This will disable the module on selected devices
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
       
	</ul>        