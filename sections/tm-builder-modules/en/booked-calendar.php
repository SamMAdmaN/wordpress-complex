<h3>Booked Calendar</h3>
<p>This module allows to create various kinds of Booking Calendars with custom time fields. Each created calendar can be assigned to a particular  user.   
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Calendar ID</dt>
                <dd>
    				Insert the calendar ID. You can find it at the Appointments >Settings > Display Custom Calendar.  
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Year </dt>
                <dd>
    				Specify the year
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Month</dt>
                <dd>
                    Specify the month
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Show calendar switcher?</dt>
                <dd>
                    Show/hide calendar switcher
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Calendar size</dt>
                <dd>
                    Specify the size of the calendar
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
