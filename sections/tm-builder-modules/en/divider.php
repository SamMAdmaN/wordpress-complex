<h3>Divider</h3>
<p>This module is used for adding custom horizontal lines to divide the elements and make the content look more structured and organized. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Color</dt>
                <dd>
    				This will adjust the color of the 1px divider line.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Visibility </dt>
                <dd>
    				This settings turns on and off the 1px divider line, but does not affect the divider height.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will turn thumbnails on and off.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        