<h3>Image</h3>
<p>Use this module to add various images to the web pages. It also offers multiple options to give the images more live and dynamic look.  
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Image URL</dt>
                <dd>
    				Upload your desired image, or type in the URL to the image you would like to display.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image Alternative Text:  </dt>
                <dd>
    				This defines the HTML ALT text. A short description of your image can be placed here.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image Title Text</dt>
                <dd>
                    This defines the HTML Title text.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Open in Lightbox</dt>
                <dd>
                    Here you can choose whether or not the image should open in Lightbox. Note: if you select to open the image in Lightbox, url options below will be ignored.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Link URL</dt>
                <dd>
                    If you would like your image to be a link, input your destination URL here. No link will be created if this field is left blank.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Url Opens</dt>
                <dd>
                    Here you can choose whether or not your link opens in a new window.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Animation</dt>
                <dd>
                    This controls the direction of the lazy-loading animation.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Remove Space Below The Image</dt>
                <dd>
                    Here you can choose whether or not the image should have a space below it.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Image Alignment</dt>
                <dd>
                    Here you can choose the image alignment.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will disable the module on selected devices
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        