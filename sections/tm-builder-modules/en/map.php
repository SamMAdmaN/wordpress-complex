<h3>Map</h3>
<p>This module is designed to embed  Google Maps  on the page in several clicks without no coding skills at all.  
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title</dt>
                <dd>
    				The title will be used within the tab button for this tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Map Pin Address  </dt>
                <dd>
    				Enter an address for this map pin, and the address will be geocoded and displayed on the map below.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Content</dt>
                <dd>
                    Here you can define the content that will be placed within the infobox for the pin.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Map Center Address</dt>
                <dd>
                    Enter an address for the map center point, and the address will be geocoded and displayed on the map below.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Icon url</dt>
                <dd>
                    Upload your marker, or type in the URL to the marker you would like to display.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Map style</dt>
                <dd>
                    Specify the Map style
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Mouse Wheel Zoom</dt>
                <dd>
                    Here you can choose whether the zoom level will be controlled by mouse wheel or not.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will disable the module on selected devices
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        