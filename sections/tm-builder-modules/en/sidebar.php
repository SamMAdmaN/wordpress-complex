<h3>Sidebar</h3>
<p>This Sidebar module allows to create an unlimited number of sidebars and add them to any page of the website. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Orientation</dt>
                <dd>
    				Choose which side of the page your sidebar will be on. This setting controls text orientation and border position.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Widget Area </dt>
                <dd>
    				Select a widget-area that you would like to display. You can create new widget areas within the Appearances > Widgets tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will turn thumbnails on and off.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        