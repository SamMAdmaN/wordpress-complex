<h3>Tabs</h3>
<p>Make any content of your website look clean and structured with the help of this Tabs module. Thanks to a fully responsive layout the modules will adjust to any screen resolution and make the information look clear on any device.   
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title</dt>
                <dd>
    				The title will be used within the tab button for this tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Content</dt>
                <dd>
    				Here you can define the content that will be placed within the current tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                    This will disable the module on selected devices.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        