<h3>Toggle</h3>
<p>Toggles can also be used to display information in a more compact and structured way. 
</p>

<h4>Module Options</h4>
	<ul class="marked-list">
		<li>
            <dl class="inline-term">
                <dt>Title </dt>
                <dd>
    				The toggle title will appear above the content and when the toggle is closed.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>State</dt>
                <dd>
                    Choose whether or not this toggle should start in an open or closed state.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Content </dt>
                <dd>
    				Here you can define the content that will be placed within the current tab.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on </dt>
                <dd>
    				This will disable the module on selected devices.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the module in the builder for easy identification.
                </dd>
            </dl>
        </li>
	</ul>        