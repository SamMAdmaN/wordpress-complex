<h3>Layout Options</h3>
<p>To make the procedure of designing and customizing the site even more comfortable,  the builder offers you an assortment of layout options. All of them are mentioned below:
</p>

<h4>Save to Library </h4>

<p>You can save any custom layout you have built and use it later, on another page or on another website. Just press the <strong>“Save to Library”</strong> button at the top of the panel and the layout will be added to the library. </p>


<figure class="img-polaroid">
        <img src="img/builder/save-to-library.png">
</figure>

<p>
	In the popup window you only need to type down the name of the layout you want to save and press the <strong>"Save"</strong> button.
</p>

<figure class="img-polaroid">
        <img src="img/builder/save-to-library-pop.png">
</figure>

<h4>Load from Library </h4>

<p>Once you need to insert the layout created earlier, simply click <strong>“Load from Library” > “Add from library”</strong> and find the saved layout and press <strong>“Load”</strong>. </p>


<figure class="img-polaroid">
        <img src="img/builder/load-layout.png">
</figure>

<h4>Clear layout </h4>

<p>This option allows you to erase all the changes and start building the layout from scratch. Note that all the changes will be deleted,  so make sure that you won’t need this layout is saved in the library. 
 </p>


<figure class="img-polaroid">
        <img src="img/builder/clear-layout.png">
</figure>