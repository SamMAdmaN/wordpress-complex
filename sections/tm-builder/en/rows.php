<h3>Rows</h3>

<p>These elements are added inside the Sections. There are also different types of <strong>Rows</strong> in which you can add the necessary modules to give the site any functionality and look.  All the rows are fully responsive and will look and perform well on any screen resolution.</p>

<figure class="img-polaroid">
        <img src="img/builder/rows.png">
</figure>

<h4>Row Properties</h4>

<p>Rows also can be customized exactly the same way as the sections. After selecting a proper row type, you can start customizing it in accordance with the specifications of your project.   
</p>

<figure class="img-polaroid">
        <img src="img/builder/row-properties-1.png">
</figure>

	<ul class="marked-list">
        <li>
            <dl class="inline-term">
                <dt>Make This Row Fullwidth </dt>
                <dd>
    				Enable this option to extend the width of this row to the edge of the browser window.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Use Custom Width</dt>
                <dd>
                    Change to Yes if you would like to adjust the width of this row to a non-standard width.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Use grid padding</dt>
                <dd>
                   Change to No if you would like delete grid padding.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Custom Padding</dt>
                <dd>
                  Adjust padding to specific values, or leave blank to use the default padding.
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Keep Custom Padding on Mobile</dt>
                <dd>
                   Allow custom padding to be retained on mobile screens. 
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Disable on</dt>
                <dd>
                   This will disable the module on selected devices
                </dd>
            </dl>
        </li>
        <li>
            <dl class="inline-term">
                <dt>Admin Label</dt>
                <dd>
                    This will change the label of the row in the builder for easy identification when collapsed.
                </dd>
            </dl>
        </li>
    </ul>
