<h3>Product</h3>
<p>
	Show a single product by ID or SKU.
</p>

<h4>[product id="99"]</h4>
<h4>[product sku="FOO"]</h4>

<h5>Args:</h5>
<div class="codebox">
	array (
	    <span>'per_page' => '12',</span>
	    <span>'columns' => '4',</span>
	    <span>'orderby' => 'date',</span>
	    <span>'order' => 'desc'</span>
	 )
 </div>
 <div class="alert alert-info">
	To find the Product ID, go to the Products screen, hover over the product and the ID appears.
</div>
<h5 class="italic">NOTE: If the product isn’t showing, make sure it isn’t set to Hidden in the Catalog Visibility.</h5>