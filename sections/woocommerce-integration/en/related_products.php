<h3>Related Products</h3>
<p>
	List related products.
</p>

<h4>[related_products per_page="12"]</h4>

<h5>Args:</h5>
<div class="codebox">
	array (
	    <span>'per_page' => '12',</span>
	    <span>'columns' => '4',</span>
	    <span>'orderby' => 'title'</span>
	 )
 </div>
 <h4>The ‘per_page’ Argument</h4>
 <div class="alert alert-info">
	<strong>Please note:</strong> the ‘per_page’ shortcode argument will determine how many products are shown on a page. This will not add pagination to the shortcode.
</div>