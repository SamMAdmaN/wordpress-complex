<h3>Yith woocommerce zoom magnifier</h3>

<p>
	Offer to your visitors a chance to inspect in detail the quality of your products. YITH WooCommerce Zoom Magnifier enables you to add a zoom effect to product images on your WooCommerce site. The WordPress plugin also adds a slider below the featured image with your product gallery images.
</p>
<p>
	Simply install/activate it and the plugin adds a zoom effect to your product detail images. You can even arrange your gallery images into a carousal slider that displays nicely under the featured image. The plugin settings are user friendly and give you the ability to tailor it to fit your unique eCommerce site.
</p>

