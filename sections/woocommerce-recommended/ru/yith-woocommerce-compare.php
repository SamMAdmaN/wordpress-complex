<h3>Yith woocommerce compare</h3>

<p>
    YITH WooCommerce Compare позволяет вам сравнивать множество товаров из вашего магазина в одной сравнительной таблице.
</p>

<p>
    YITH WooCommerce Compare плагин это расширение для Woocommerce плагина, которое позволяет вашим клиентам сравнивать товары из вашего магазина. Все сохраненные продукты выводятся в сравнительной таблице где пользователь может проанализивароть основные отличия между ними. Особенность каждого продукта можно настроить c woocommerce атрибутами в конфигурации продукта. Также, вы можете добавить виджет со списком продуктов добавленых пользователей, где можно управлять ими. Кроме того, можно очень легко настроить стиль таблицы сравнения, настраивая специальный шаблон.
</p>

<h4>Конфигурация</h4>

<ul class="marked-list">
    <li>
        Add the features in each product by setting the attributes in prouct configuration
    </li>
    <li>
        Configure the options in YIT Plugin -> Compare
    </li>
    <li>
        The user of your shop will click in the "Compare" button located in the single product page
    </li>
    <li>
        Will be open a popup with the compare table inside.
    </li>
</ul>
<p>
    You can also add the widget with the product list of compare table, in Appearance -> Widgets and adding the widget "YITH WooCommerce Compare Widget".
</p>

